package com.ciandt.investment.core.domain;

public class Fundo {
	
	private String cnpj;
	private Double captacaoLiquida;
	
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public Double getCaptacaoLiquida() {
		return captacaoLiquida;
	}
	public void setCaptacaoLiquida(Double captacaoLiquida) {
		this.captacaoLiquida = captacaoLiquida;
	}

}
